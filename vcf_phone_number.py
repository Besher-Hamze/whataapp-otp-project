sorted_codes = sorted(country_codes.keys(), key=len, reverse=True)

# Group numbers by country code
country_groups = defaultdict(list)

for num in phone_numbers:
    if not num.startswith('+') or len(num) < 2:
        continue  # Skip invalid
    digits = num[1:]
    country_code = None
    for code in sorted_codes:
        if digits.startswith(code):
            country_code = code
            break
    if country_code:
        country_groups[country_code].append(num)
    else:
        country_groups['UNKNOWN'].append(num)

